def write_file(path, lines):
    """Write lines to path as UTF-8; return True on success."""
    content = "\n".join(lines)
    path.write_text(content, encoding="utf-8")
    return True


def main():
    """Check existence, write files, validate each with py_compile + ast.parse."""
    files = [
        (SIG_OUT, build_lines_regression_channel),
        (TST_OUT, build_lines_test_regression_channel),
    ]

    for path, builder in files:
        print("")
        print("--- " + str(path))
        if path.exists():
            print("  WARNING: file already exists, aborting write for this file.")
            ERRORS.append(str(path) + " [EXISTS]")
            continue
        lines = builder()
        write_file(path, lines)
        print("  WRITTEN:    " + str(path))
        if validate_output(path):
            print("  [PASS] " + str(path))
        else:
            ERRORS.append(str(path))
            print("  [FAIL] " + str(path))

    print("")
    if ERRORS:
        print("BUILD FAILED — errors in: " + ", ".join(ERRORS))
        sys.exit(1)
    else:
        print("BUILD OK — all files compiled clean")


if __name__ == "__main__":
    main()
