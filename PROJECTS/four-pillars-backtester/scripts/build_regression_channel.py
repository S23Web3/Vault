"""
Build script: writes signals/regression_channel.py and scripts/test_regression_channel.py.
Run: python scripts/build_regression_channel.py
"""
import ast
import os
import py_compile
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
SIG_OUT = ROOT / "signals" / "regression_channel.py"
TST_OUT = ROOT / "scripts" / "test_regression_channel.py"

ERRORS = []


def validate_output(path):
    """Run py_compile then ast.parse on a written .py file; return True if both pass."""
    path_str = str(path)
    try:
        py_compile.compile(path_str, doraise=True)
        print("  SYNTAX OK:  " + path_str)
    except py_compile.PyCompileError as e:
        print("  SYNTAX FAIL: " + str(e))
        return False
    try:
        source = Path(path_str).read_text(encoding="utf-8")
        ast.parse(source, filename=path_str)
        print("  AST OK:     " + path_str)
    except SyntaxError as e:
        print("  AST FAIL line " + str(e.lineno) + ": " + str(e.msg) + " in " + path_str)
        return False
    return True


def build_lines_regression_channel():
    """Return list of source lines for signals/regression_channel.py."""
    L = []
    L.append('"""')
    L.append("Regression channel module: pre-stage-1 orderly decline gate and stage-1-to-stage-2 anchored channel.")
    L.append("")
    L.append("Integration context (state machine caller, do not implement here):")
    L.append("")
    L.append("  At Stage 1 entry (stoch_9 crosses below 25 for first time):")
    L.append("    self.stage1_bar = current_bar_index")
    L.append("    pre_window = prices[current_bar - pre_lookback : current_bar + 1]")
    L.append("    self.pre_ch = fit_channel(pre_window)")
    L.append("    self.ch_gate_passed = pre_stage1_gate(self.pre_ch)")
    L.append("")
    L.append("  At Stage 2 exit (stoch_9 crosses above 25, divergence confirmed):")
    L.append("    if not self.ch_gate_passed:")
    L.append("        reject signal")
    L.append("    anchored = compute_channel_anchored(prices, self.stage1_bar, current_bar)")
    L.append("    if price_in_lower_half(current_price, anchored.center_at_last):")
    L.append("        fire entry signal")
    L.append("    else:")
    L.append("        reject signal")
    L.append('"""')
    L.append("from collections import namedtuple")
    L.append("import numpy as np")
    L.append("")
    L.append("")
    L.append("ChannelResult = namedtuple(")
    L.append('    "ChannelResult",')
    L.append('    ["slope", "slope_pct", "r_squared", "band_width_pct", "center_at_last", "std_residuals"],')
    L.append(")")
    L.append("")
    L.append("_ZERO = ChannelResult(")
    L.append("    slope=0.0,")
    L.append("    slope_pct=0.0,")
    L.append("    r_squared=0.0,")
    L.append("    band_width_pct=0.0,")
    L.append("    center_at_last=0.0,")
    L.append("    std_residuals=0.0,")
    L.append(")")
    L.append("")
    L.append("")
    L.append("def fit_channel(prices) -> ChannelResult:")
    L.append('    """Fit linear regression to prices; return ChannelResult or zero on degenerate input."""')
    L.append("    arr = np.asarray(prices, dtype=float)")
    L.append("    n = len(arr)")
    L.append("    if n < 3:")
    L.append("        return _ZERO")
    L.append("    x = np.arange(n, dtype=float)")
    L.append("    mean_x = np.mean(x)")
    L.append("    mean_y = np.mean(arr)")
    L.append("    ss_xx = np.sum((x - mean_x) ** 2)")
    L.append("    if ss_xx == 0.0:")
    L.append("        return _ZERO")
    L.append("    slope = np.sum((x - mean_x) * (arr - mean_y)) / ss_xx")
    L.append("    intercept = mean_y - slope * mean_x")
    L.append("    fitted = intercept + slope * x")
    L.append("    residuals = arr - fitted")
    L.append("    ss_res = np.sum(residuals ** 2)")
    L.append("    ss_tot = np.sum((arr - mean_y) ** 2)")
    L.append("    r_squared = 0.0 if ss_tot == 0.0 else 1.0 - ss_res / ss_tot")
    L.append("    center_at_last = intercept + slope * (n - 1)")
    L.append("    std_res = float(np.std(residuals))")
    L.append("    slope_pct = slope / mean_y if mean_y != 0.0 else 0.0")
    L.append("    band_width_pct = (2.0 * std_res / mean_y) if mean_y != 0.0 else 0.0")
    L.append("    return ChannelResult(")
    L.append("        slope=float(slope),")
    L.append("        slope_pct=float(slope_pct),")
    L.append("        r_squared=float(r_squared),")
    L.append("        band_width_pct=float(band_width_pct),")
    L.append("        center_at_last=float(center_at_last),")
    L.append("        std_residuals=float(std_res),")
    L.append("    )")
    L.append("")
    L.append("")
    L.append("def extrapolate_center(channel: ChannelResult, bars_elapsed: int) -> float:")
    L.append('    """Extend regression center forward by bars_elapsed bars past the fit window."""')
    L.append("    if bars_elapsed <= 0:")
    L.append("        return channel.center_at_last")
    L.append("    return channel.center_at_last + channel.slope * bars_elapsed")
    L.append("")
    L.append("")
    L.append("def price_in_lower_half(price: float, center: float) -> bool:")
    L.append('    """Return True if price is strictly below center (regression midline)."""')
    L.append("    return price < center")
    L.append("")
    L.append("")
    L.append("def compute_channel_anchored(all_prices, start_idx: int, end_idx: int) -> ChannelResult:")
    L.append('    """Compute regression over all_prices[start_idx:end_idx+1]; return zero if window < 3."""')
    L.append("    window = all_prices[start_idx : end_idx + 1]")
    L.append("    return fit_channel(window)")
    L.append("")
    L.append("")
    L.append("def pre_stage1_gate(channel: ChannelResult, r2_min: float = 0.45, slope_pct_max: float = -0.001) -> bool:")
    L.append('    """Return True if channel shows orderly negative slope with sufficient R-squared."""')
    L.append("    return channel.r_squared > r2_min and channel.slope_pct < slope_pct_max")
    L.append("")
    return L


